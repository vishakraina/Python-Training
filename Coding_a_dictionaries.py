'''
Suppose we want to create a dictionary for customer 29876 with these details:

First Name: David
Last Name: Elliott
Address: 4803 Wellesley St.

'''

customer_29876 = {
    "first name": "David",
    "last name": "Elliott",
    "address": "4803 Wellesley St."
}

# Comparing Lists and Dictionaries.

''' 
Variable Name and Equal Sign: 
Both dictionaries and lists begin with a variable name and an equal sign:

'''

jobs_to_do = ["email", "texting", "calls"]  # List
customer_29876 = {"first name": "David", "last name": "Elliott", "address": "4803 Wellesley St."}  # Dictionary

'''
Items Separated by Commas: 
Both data structures separate items with commas:
'''

jobs_to_do = ["email", "texting", "calls"]  # List
customer_29876 = {"first name": "David", "last name": "Elliott", "address": "4803 Wellesley St."}  # Dictionary

'''
Enclosure in Brackets:

List: Enclosed in square brackets []
Dictionary: Enclosed in curly brackets {}

'''

jobs_to_do = ["email", "texting", "calls"]  # List uses square brackets
customer_29876 = {"first name": "David", "last name": "Elliott", "address": "4803 Wellesley St."}  # Dictionary uses curly brackets

'''
Structure of Items:

List: Each item is a single element.
Dictionary: Each item is a key-value pair, separated by a colon :.

'''

customer_29876 = {
    "first name": "David",  # Key: "first name", Value: "David"
    "last name": "Elliott",  # Key: "last name", Value: "Elliott"
    "address": "4803 Wellesley St."  # Key: "address", Value: "4803 Wellesley St."
}

