# Topic 41 - Functions

# Basic Code Without a Function:

first_number = 2
second_number = 3
total = first_number + second_number
print(total)  # Output: 5

# Creating a Function:

def add_numbers():
    first_number = 2
    second_number = 3
    total = first_number + second_number
    print(total)


# Calling a Function:

add_numbers()  # Output: 5


