# Topic 9 - If Statements

# What is an If Statement?

species = "cat"
if species == "cat":
    print("Yep, it's a cat.")

# Basic If Statement Syntax

species = "cat"
if species == "cat":
    print("Yep, it's a cat.")

# Multiple Actions for a Single Condition

if species == "cat":
    status = "ok"
    kingdom = "animal"
    print("Yep, it's a cat.")

# Using If Statements with Numbers

if 2 + 2 == 4:
    print("Everything makes sense.")

# Indentation Rules in If Statements

number_of_husbands = 1
if number_of_husbands == 1:
    print("So far so good.")
    print("Congratulations.")
    print("All done")

