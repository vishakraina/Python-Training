# Topic 8 - Concatenating Text Strings

# Example of Concatenation:

greeting = "Hello"
addressee = "World"
whole_greeting = greeting + addressee
print(whole_greeting)  # Output: HelloWorld


# 1.	Concatenating Variables

greeting = "Hello"
addressee = "World"
whole_greeting = greeting + ", " + addressee + "!"
print(whole_greeting)  # Output: Hello, World!

#  2.	Concatenating Directly

print("Hello, " + "World!")  # Output: Hello, World!

# 3.	Mixing Variables and Strings

punc = "!"
print("Hello" + ", " + "World" + punc)  # Output: Hello, World!

# 4.	Concatenation Rules

print("The sum of 2 plus 2 is " + "4")  # Output: The sum of 2 plus 2 is 4
