# Math Expressions: Familiar Operators

#Math Expressions in Python

2 + 2

# Assigning Expressions to Variables
popular_number = 2 + 2
print(popular_number)      # Output: 4

loss = 12 - 24
print(loss)                # Output: -12

# Using Multiplication and Division

dozens = 3 * 12
print(dozens)              # Output: 36

popular_number = 12 / 4
print(popular_number)      # Output: 3

#Mixing Variables and Numbers in Expressions

num = 0.075
total = num + 200
print(total)               # Output: 200.075

# Using Only Variables in Expressions

num = 10
another_num = 1.5
sum_of_numbers = num + another_num
print(sum_of_numbers)      # Output: 11.5

