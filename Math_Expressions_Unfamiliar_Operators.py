# Topic 6 - Math Expressions: Unfamiliar Operators

# The Modulo Operator %

whats_left_over = 10 % 3

whats_left_over = 9 % 3
# whats_left_over will be 0

# Incrementing and Decrementing with += and -=

age = 54
age += 1
# age now becomes 55

age = 12
age += 50   # age becomes 62
age -= 2    # age becomes 60

# Multiplication Shorthand with *=

age = 12
age *= 3    # age becomes 36

# Combining Variables

age = 12
amount_to_increment = 3
age += amount_to_increment  # age becomes 15
